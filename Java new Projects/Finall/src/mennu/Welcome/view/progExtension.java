
package mennu.Welcome.view;
public class progExtension {

    private String progId,progname ;

    
    public progExtension(){}
    
    public progExtension(String _progId,String _progname){
        this.progId = _progId;
        this.progname = _progname;
         
        
    }
    public String getProgId() {
        return progId;
    }

    public String getProgname() {
        return progname;
    }


}    

