  <div class="title"><h3 align="center">Background profile of the organization</h3></div>
<h6>Alfa Youth Organization came into existence in the year 2011 with a registration certificate in the ministry of SDGO with 12 members and later increased to 35 members while the number rises annually. We began as a faith based organization where by the youths from various churches united and made an idea of grouping to exist in their midst. The current number of members is covering the whole Kericho County and hopefully soon we will incorporate members from all over the country as a SACCO.
  We have members who are still students in various institutions i.e. Kenyatta University, University of Eldoret, Mount Kenya University, Moi University, Meru University, University of Kabianga, KMTC and various colleges. Luckily, some members are currently working in various professionalism i.e. Uniliver as supervisor, two in Tenwek Hospital as clinical officer and another one as nurse, Lab technician, Factories as electrical and mechanical engineers, others pastors in various churches, business persons in the nearest town, teachers and others in farming. Only a few of them completed their O level recently and hoping to proceed in their studies soon.
  We have been well known in upgrading the status and welfare of the community in terms of development and by preaching peace, uniting people, teaching the community in various sections, holding forums to inform the community in usual governments’ updates. The group as well strengthens by our honorary members who are always willing to give us full support at all times. Honorary members includes; Hon. Mrs Rose Sang (wife to MP Bureti), Mrs Beatrice Chepkwony (Lecturer University of Kabianga), Ann Kesir (CACC Bureti), Ann Chebet (Administrator Ndanai/Abosi Ward), Mr. Ng’erechi (Sports officer Kericho county), Chiefs and assistants (Chemosot ward), Mr. Yegon Charles (Bureti Youth officer) and Sub-county commissioner.</h6>
