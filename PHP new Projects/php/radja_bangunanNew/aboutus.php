	<h3><font color="red">Radja</font> <font color="green">Bangunan</font></h3><br/>
	<p>
        <font face:'comic sans';>Salah satu kebutuhan primer manusia adalah memiliki tempat tinggal. Pada awalnya fungsi rumah hanya untuk bertahan diri. Namun lama kelamaan fungsi tersebut telah berubah menjadi tempat tinggal keluarga. Oleh karena itulah tuntutan untuk bisa memiliki rumah yang bagus, indah serta nyaman menjadi impian semua orang saat ini. Bahkan merujuk kepada data Biro Pusat Statistik (BPS) bahwa kebutuhan rumah di Indonesia pada tahun 2010 telah mencapai 13 juta unit, angka tersebut memiliki kenaikan yang signifikan (sekitar 60%) jika dibandingkan pada tahun 2009 yang hanya mencapai 8 juta unit.</font></p>
        
      <p>  Ketersediaan material bahan bangunan merupakan faktor yang sangat menyulitkan bagi pemilik rumah. Hal ini disebabkan dengan banyaknya ragam material bahan bangunan yang akan digunakan untuk sebuah rumah, ditambah lagi dengan lokasi penjualan yang berbeda- beda bahkan berjauhan untuk tiap jenis bahan bangunan nya. Hal penting lain yang sangat krusial adalah keinginan dari pemilik rumah untuk bisa memilih material yang cocok dengan selera dan budget yang telah dianggarkan sebelumnya.</p>

    <p>Kategori produk yang tersedia sangat lengkap mulai dari Building Material seperti kayu, triplek, pasir serta batu bata hingga genteng rumah, Cat dinding untuk dalam dan luar ruangan, Keramik lantai dan dinding, Toilet dan Perlengkapan Kamar Mandi, Perlengkapan Listrik, Alat Pertukangan, Pintu dan Jendela. Segala sesuatu yang berhubungan dengan rumah semuanya tersedia di Radja Bangunan.</p>
	
