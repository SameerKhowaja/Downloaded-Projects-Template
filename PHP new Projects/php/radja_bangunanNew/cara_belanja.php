<strong>Cara Belanja</strong><br>
<p>
Berbelanja bersama kami juga tidak kalah mudahnya, Anda bisa langsung cari kategori barang yang Anda inginkan atau search barang yang Anda mau.
Kemudian  Anda bisa mengikuti panduan di bawah ini :
<br><br>
<ol>
    <li>Untuk mengetahui detail spesifikasi barang yang Anda inginkan, Anda bisa klik “View” terlebih dahulu</li>
    <li>Jika sesuai dengan yang Anda butuhkan, Anda bisa langsung klik Add to Cart. Anda bisa melanjutkan berbelanja produk lain “Continue Shopping” dengan cara yang sama, atau langsung melanjutkan ke proses pembayaran dengan Klik “Lanjutkan ke Pembayaran”..</li>
    <li>Cek kembali alamat tujuan pengiriman barang yang Anda pesan, apabila berbeda dari alamat registrasi maka Anda bisa menggantinya.</li>
    <li>Pilih Metode Pembayaran yang Anda inginkan : Kartu Kredit atau Transfer Tunai.</li>
    <li>Anda juga bisa memilih tipe pengiriman apakag regular, economy atau ONS.</li>
    <li>Setelah Anda mendapatkan total yang harus ditransfer, Anda bisa langsung melakukan transaksi sesuai metode pembayaran yang Anda pilih.</li>
    <li>Setelah selesai, jangan lupa untuk melakukan konfirmasi pembayaran pada menu Order History dan Anda akan mendapatkan berita terbaru mengenai perkembangan pesanan Anda di sana.</li>    
    <li>Barang akan tiba dalam waktu estimasi yang telah diberikan.</li>
</ol></p>		
