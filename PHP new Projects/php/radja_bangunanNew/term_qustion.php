Selain melalui transfer tunai ke bank yang telah ditunjuk, Anda juga bisa melakukan transaksi di Radja Bangunan Online dengan menggunakan kartu kredit berlogo Visa/mastercard melalui halaman payment gateaway yang berkredibilitas dalam mata uang Rupiah. Proses pembayaran Anda juga kami pastikan keamanannya dengan protocol standard SSL (Secure Socket Layer), sehingga Anda tak perlu khawatir mengenai kemungkinan pencurian informasi kartu kredit.
